<template>
    <div class="form-input ">
        <form class="mt-4">   
          <label for="default-search" class="mb-2 text-sm font-medium text-gray-900 sr-only dark:text-white">Buscar</label>
          <div class="relative">
            <div class="absolute inset-y-0 left-0 flex items-center pl-3 pointer-events-none">
                <svg class="w-4 h-4 text-gray-500 dark:text-gray-400" aria-hidden="true" xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 20 20">
                    <path stroke="currentColor" stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="m19 19-4-4m0-7A7 7 0 1 1 1 8a7 7 0 0 1 14 0Z"/>
                </svg>
            </div>
                <input type="search" id="default-search" 
                class="block w-full p-4 pl-10 text-sm text-gray-900 border border-gray-300 rounded-lg bg-gray-50 focus:ring-blue-500 focus:border-blue-500 dark:bg-gray-700 dark:border-gray-600 dark:placeholder-gray-400 dark:text-white dark:focus:ring-blue-500 dark:focus:border-blue-500" 
                placeholder="Buscar por Nombre o ID" required
                v-model="nombrePokemon">
                <button 
                type="submit" 
                class="text-white absolute right-2.5 bottom-2.5 bg-gradient-to-r from-cyan-400 via-cyan-500 to-cyan-600 hover:bg-blue-800 focus:ring-4 focus:outline-none focus:ring-cyan-300 font-medium rounded-lg text-sm px-4 py-2 dark:bg-blue-600 dark:hover:bg-blue-700 dark:focus:ring-blue-800"
                @click.prevent="buscarPokemon"
                >Buscar</button>
            </div>
        </form>
    </div>
</template>

<script>
export default {
    name: 'buscador',
    // props: {},
    data: function(){
        return {
            nombrePokemon:'',
        }
    },
    // computed: {},
    methods: {
        buscarPokemon(){
            if(this.nombrePokemon == ''){
                alert('Debe Ingresar un Nombre o id de pokemon')
                return
            }
            this.$emit('buscarPokemon',this.nombrePokemon)
            this.nombrePokemon = ''
        }
    },
    // watch: {},
    // components: {},
    // mixins: [],
    // filters: {},
    // -- Lifecycle Methods
    // -- End Lifecycle Methods
}
</script>

